         public void clock(){
        Thread t = new Thread(){

            @Override
            public void run() {
                
                try {
                    while(true){
                        
                        
                    
                     Calendar calc = Calendar.getInstance();
                             //new GregorianCalendar();
                     int hour = calc.get(Calendar.HOUR);
                     int minute = calc.get(Calendar.MINUTE);
                     int second = calc.get(Calendar.SECOND);
                     
                    // 
                     int day = calc.get(Calendar.DAY_OF_MONTH);
                     int week = calc.get(Calendar.WEEK_OF_MONTH);
                     int month = calc.get(Calendar.MONTH)+1;
                     int year = calc.get(Calendar.YEAR);
                     jLabel1.setText(hour+":"+minute+":"+second);
                     jLabel2.setText(year+"/"+month+"/"+day);

                    
                    sleep(1000);
                }
                } catch (InterruptedException ex) {
                    Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
                }
               
                // super.run(); //To change body of generated methods, choose Tools | Templates.
                
            }
            
         
        };
        t.start();
    }
    
    
    
